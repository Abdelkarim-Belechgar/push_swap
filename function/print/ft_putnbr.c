#include "../../include/stack.h"

void	ft_putnbr(int nbr)
{
	if (nbr == -2147483648)
	{
		ft_putstr("-2147483648", 1);
		return ;
	}
	if (nbr < 0)
	{
		ft_putchar('-', 1);
		ft_putnbr(-nbr);
		return ;
	}
	if (nbr > 9)
		ft_putnbr(nbr / 10);
	ft_putchar("0123456789"[nbr % 10], 1);
}
